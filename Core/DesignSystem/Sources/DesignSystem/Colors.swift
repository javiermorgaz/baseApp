import SwiftUI

// MARK: View Colors
public extension Color {
    static let primaryColor = Color("primary", bundle: .module)
    static let secondaryColor = Color("secondary", bundle: .module)
    static let primaryBackgroundColor = Color("primaryBackground", bundle: .module)
}

// MARK: Font Colors
public extension Color {
    static let onPrimaryColor = Color("onPrimary", bundle: .module)
    static let onPrimaryLightColor = Color("onPrimaryLight", bundle: .module)
    static let onSecondaryColor = Color("onSecondary", bundle: .module)
}
